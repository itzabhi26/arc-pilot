// Compiles contracts/*.sol with solc and writes ABI + bytecode as a typed
// TS module. Run with: node scripts/compile-contracts.mjs
import fs from "node:fs";
import path from "node:path";
import solc from "solc";

const root = path.resolve(import.meta.dirname, "..");
const contractsDir = path.join(root, "contracts");

function readSource(file) {
  return fs.readFileSync(path.join(contractsDir, file), "utf8");
}

const input = {
  language: "Solidity",
  sources: {
    "SmartWallet.sol": { content: readSource("SmartWallet.sol") },
    "SmartWalletFactory.sol": { content: readSource("SmartWalletFactory.sol") },
  },
  settings: {
    optimizer: { enabled: true, runs: 200 },
    outputSelection: {
      "*": { "*": ["abi", "evm.bytecode.object"] },
    },
  },
};

function findImports(importPath) {
  try {
    return { contents: fs.readFileSync(path.join(contractsDir, importPath), "utf8") };
  } catch {
    return { error: `File not found: ${importPath}` };
  }
}

const output = JSON.parse(solc.compile(JSON.stringify(input), { import: findImports }));

const errors = (output.errors || []).filter((e) => e.severity === "error");
if (errors.length > 0) {
  errors.forEach((e) => console.error(e.formattedMessage));
  process.exit(1);
}
(output.errors || [])
  .filter((e) => e.severity !== "error")
  .forEach((e) => console.warn(e.formattedMessage));

const wallet = output.contracts["SmartWallet.sol"].SmartWallet;
const factory = output.contracts["SmartWalletFactory.sol"].SmartWalletFactory;

const tsOut = `// AUTO-GENERATED by scripts/compile-contracts.mjs — do not edit by hand.
// Regenerate with: node scripts/compile-contracts.mjs

export const SMART_WALLET_ABI = ${JSON.stringify(wallet.abi, null, 2)} as const;
export const SMART_WALLET_BYTECODE = "0x${wallet.evm.bytecode.object}";

export const SMART_WALLET_FACTORY_ABI = ${JSON.stringify(factory.abi, null, 2)} as const;
export const SMART_WALLET_FACTORY_BYTECODE = "0x${factory.evm.bytecode.object}";
`;

const outPath = path.join(root, "src", "lib", "wallet", "smart-wallet-artifacts.ts");
fs.writeFileSync(outPath, tsOut);
console.log(`Wrote ${path.relative(root, outPath)}`);

// Plain JSON copy (no TS syntax) so plain Node scripts, like the deploy
// script, can read it without a TS loader.
const jsonOut = {
  smartWallet: { abi: wallet.abi, bytecode: `0x${wallet.evm.bytecode.object}` },
  smartWalletFactory: { abi: factory.abi, bytecode: `0x${factory.evm.bytecode.object}` },
};
const jsonPath = path.join(root, "contracts", "artifacts.json");
fs.writeFileSync(jsonPath, JSON.stringify(jsonOut, null, 2));
console.log(`Wrote ${path.relative(root, jsonPath)}`);
